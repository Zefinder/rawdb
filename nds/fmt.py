
class FormatIterator:
    def __init__(self, fmt):
        self.fmt = list(fmt)
    def __next__(self):
        s = ""
        if not self.fmt:
            raise StopIteration
        c = self.fmt.pop(0)
        while c == "x":
            if not self.fmt:
                raise StopIteration
            c = self.fmt.pop(0)
        hasnum = False
        while c.isdigit():
            hasnum = True
            s += c
            c = self.fmt.pop(0)
        if hasnum:
            if c != 's':
                num = int(s)
                newfmt = []
                for i in range(num):
                    newfmt.append(c)
                newfmt.extend(self.fmt)
                self.fmt = newfmt
                return self.fmt.pop(0)
        s += c
        return s
    def __iter__(self):
        return self

